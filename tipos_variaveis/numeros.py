# Inteiro
numero_inteiro = 3
print("Inteiro = ", numero_inteiro)

# Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

# type()
print("Tipo da variavel inteiro = ", type(numero_inteiro))
print("Tipo da variavel real = ", type(numero_real))

# Soma +
soma = 1 + 1
print("1 + 1 = ", soma)

# Subtracao -
subtracao = 1 - 1
print ("1 - 1 =", subtracao)

# Multiplicacao *
multiplicacao = 2 * 2
print("2 x 2 = ", multiplicacao)

# Divisao /
divisao = 5 / 2 
print ("5 / 2 = ", divisao)
print(" Tipo da variavel do resuldo da divisao = ", type(divisao))

# int()
print("valor em inteiro = ", int(divisao))

# float()
print("valor em float =", float(soma))

# modulo %
modulo = 5 % 2
print("Modulo = ", modulo)

# Divisao inteiro /
divisao = 5 // 2 
print ("5 // 2 = ", divisao)
print(" Tipo da variavel do resuldo da divisao = ", type(divisao))